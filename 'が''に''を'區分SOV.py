for i in range(20):
  L[i] = L[i].replace("ます。", "")
  print(L[i])
  if "に" in L[i]:
    S = L[i].split('が')
    O = S[1].split('に')
    print("No.", i+1, "主詞（'が'以前      ）：", S[0])
    print("No.", i+1, "受詞（'が'到'に'中間 ）：", O[0])
    print("No.", i+1, "動詞（'に'以後      ）：", O[1], '\n')
  elif "を" in L[i]:
    S = L[i].split('が')
    O = S[1].split('を')
    print("No.", i+1, "主詞（'が'以前      ）：", S[0])
    print("No.", i+1, "受詞（'が'到'を'中間 ）：", O[0])
    print("No.", i+1, "動詞（'を'以後      ）：", O[1], '\n')
  else:
    S = L[i].split('が')
    print("No.", i+1, "主詞（'が'以前      ）：", S[0])
